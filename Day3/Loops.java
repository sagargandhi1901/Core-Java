package Day3;

public class Loops {
	public static void main(String[] args) {
		
		// while loop
		int i = 0;
		while (i < 5) {
			System.out.println("hello");
			i++;
		}
		
		System.out.println();
		// do while loop
		int j = 0;
		do {
			System.out.println("Hi");
			j++;
		} while (j < 5);
		
		System.out.println();
		// for loop
		for (int k = 0; k < 5; k++) {
			System.out.println("hey");
		}
	}
}
